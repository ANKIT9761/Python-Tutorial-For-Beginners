# There are many operators in python


a = 2 
b = 3 

print(f'Exponent Operators : {a**b}')# Exponent Operators
print(f'Modulus/remainder Operators : {a%b}') # Modulus/remainder Operators
print(f'Integer division/floored quotient : {a//b}')#Integer division/floored quotient
print(f'division operators : {a/b}') # division operators
print(f'Multiplication operators : {a*b}') # Multiplication operators
print(f'Subtraction operators : {a-b}') # Subtraction operators
print(f'Addition operators : {a+b}') # Addition operators



